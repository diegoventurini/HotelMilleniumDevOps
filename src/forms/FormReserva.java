    private void btBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarClienteActionPerformed
        if (verificaCPF()) {
            Cliente c = FormPrincipal.clienteDAO.buscarCliente(tfCpf.getText());
            if (c != null) {
                tfCpf.setEnabled(false);
                
                this.pesquisouCliente = true;
                if (this.pesquisouQuarto)
                    btCadastrar.setEnabled(true);
            }
            else
                JOptionPane.showMessageDialog(null, "Cliente não encontrado", "Atenção", JOptionPane.WARNING_MESSAGE);
            
        }
        else
            JOptionPane.showMessageDialog(null, "Preencha o campo antes de pesquisar", "Atenção", JOptionPane.WARNING_MESSAGE);
        
    }//GEN-LAST:event_btBuscarClienteActionPerformed

    private void btBuscarQuartoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarQuartoActionPerformed
        if (verificaNumQuarto()) {
            Quarto q = FormPrincipal.quartoDAO.buscarQuarto(Short.parseShort(tfNumeroQuarto.getText()));
            if (q != null) {
                if (q.getOcupado()){
                    JOptionPane.showMessageDialog(null, "Esse quarto já está ocupado.", "Atenção", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                
                tfNumeroQuarto.setEnabled(false);
                this.pesquisouQuarto = true;
                this.btOrcamento.setEnabled(true);
                
                if (this.pesquisouCliente)
                    btCadastrar.setEnabled(true);
            }
            else
                JOptionPane.showMessageDialog(null, "Quarto não encontrado", "Atenção", JOptionPane.WARNING_MESSAGE);
            
        } 
        else
            JOptionPane.showMessageDialog(null, "Preencha o campo antes de pesquisar", "Atenção", JOptionPane.WARNING_MESSAGE);
        
    }//GEN-LAST:event_btBuscarQuartoActionPerformed

    private void btOrcamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOrcamentoActionPerformed
        if (verificaDiaHospedagem()){
            if (this.tfDiasHospedagem.getText().matches("[0-9]+")){
                Quarto q = FormPrincipal.quartoDAO.buscarQuarto(Short.parseShort(tfNumeroQuarto.getText()));
                float valor = q.getValor() * Integer.parseInt(tfDiasHospedagem.getText());
                lbResultado.setText(valor + "");
            }
            else
                JOptionPane.showMessageDialog(null, "O campo \"Orçamento\" não permite o uso de letras.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
        else
            JOptionPane.showMessageDialog(null, "O campo \"Orçamento\" está em branco", "Aviso", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btOrcamentoActionPerformed

