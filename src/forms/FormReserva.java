    private Boolean verificaCPF() {
        if (this.tfCpf.getText().equals("   .   .   -  ")) {
            this.tfCpf.requestFocus();
            return false;
        } else {
            return true;
        }
    }

    private Boolean verificaData() {
        if (this.tfDataInicio.getText().equals("  /  /    ")) {
            this.tfDataInicio.requestFocus();
            return false;
        } else {
            return true;
        }
    }

    private Boolean verificaNumQuarto() {
        if (this.tfNumeroQuarto.getText().equals("    ")) {
            this.tfNumeroQuarto.requestFocus();
            return false;
        } else {
            return true;
        }
    }

    private Boolean verificaMascara() {
        if (verificaCPF()) {
            if (verificaData()) {
                return verificaNumQuarto();
            }
        }

        return false;
    }

    private Boolean verificaDiaHospedagem() {
        if (this.tfDiasHospedagem.getText().equals("")) {
            tfDiasHospedagem.requestFocus();
            return false;
        } else {
            return true;
        }
    }

    private Boolean verificaCampos() {
        if (verificaMascara()) {
            return verificaDiaHospedagem();
        }

        return false;
    }
    
    private void limparDados(){
        tfCpf.setText("");
        tfNumeroQuarto.setText("");
        tfDataInicio.setText("");
        tfDiasHospedagem.setText("");
        
        tfCpf.setEnabled(true);
        tfNumeroQuarto.setEnabled(true);
        btCadastrar.setEnabled(false);
        btOrcamento.setEnabled(false);
        
        pesquisouCliente = false;
        pesquisouQuarto = false;

        tfCpf.requestFocus();
    }
    
    public void preencheDados(){
        Reserva r = new Reserva();
        
        Cliente c = FormPrincipal.clienteDAO.buscarCliente(tfCpf.getText());
        r.setCliente(c);
        
        Quarto q = FormPrincipal.quartoDAO.buscarQuarto(Short.parseShort(tfNumeroQuarto.getText()));
        r.setQuarto(q);
        
        r.setDiasHospedagem(Short.parseShort(tfDiasHospedagem.getText()));
        
        int num = FormPrincipal.reservaDAO.getList().size() + 1;
        r.setId((short) num);
        
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        try {
            df.setLenient(false);
            Date dt = df.parse(tfDataInicio.getText());
            r.setDataInicio(dt);
            
            FormPrincipal.reservaDAO.adicionarReserva(r);
            r.getQuarto().setOcupado(true);
            
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!", "Cadastro", JOptionPane.INFORMATION_MESSAGE);
            this.limparDados();
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Data Invalida", "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }

