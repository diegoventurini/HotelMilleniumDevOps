    private void btEmitirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEmitirActionPerformed
       File arq = new File("C:\\NotaFiscal_"+tfId.getText()+".txt");
       if(!arq.exists())
       {
           try {
               
               arq.createNewFile();
           } catch (IOException ex) {
               JOptionPane.showMessageDialog(null, "Não foi poossivel criar o arquivo!", "Atenção",JOptionPane.ERROR_MESSAGE);
           }
       }
       
        try {
            FileWriter fw = new FileWriter(arq);
            BufferedWriter bw = new BufferedWriter(fw);
            
            String vet[] = taDados.getText().split("\n");
            for(String linha: vet)
            {
                bw.write(linha);
                bw.newLine();
            }

            bw.close();
            fw.close();
            
            JOptionPane.showMessageDialog(null, "Nota Fiscal foi criada com sucesso em C:\\!", "Atenção",JOptionPane.INFORMATION_MESSAGE);
            
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Não foi poossivel abrir o arquivo!", "Atenção",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btEmitirActionPerformed

