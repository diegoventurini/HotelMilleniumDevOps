    private void btBuscarReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarReservaActionPerformed
        if (tfId.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Preencha o campo antes de pesquisar.", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else if (!this.tfId.getText().matches("[0-9]+")){
            JOptionPane.showMessageDialog(null, "Apenas número são permitidos no campo.", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Reserva r = FormPrincipal.reservaDAO.buscarReserva(Short.parseShort(tfId.getText()));
        taDados.setText("");
        
        if (r !=  null){
            taDados.append(r.toString() + "\n");
            taDados.append(r.getCliente().toString() + "\n");
            taDados.append(r.getQuarto().toString());
            btEmitir.setEnabled(true);
        }
        else{
            btEmitir.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Reserva não encontrada.", "Atenção", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btBuscarReservaActionPerformed

