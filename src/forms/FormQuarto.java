    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed

        if (this.verificaCampos()) {
            if (this.tfDiaria.getText().matches("[0-9.]+")) {
                
                Quarto q = FormPrincipal.quartoDAO.buscarQuarto(Short.parseShort(tfNumeroQuarto.getText()));
                if (q != null){
                    JOptionPane.showMessageDialog(null, "Já existe esse numero de quarto cadastrado.", "Aviso", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                this.preencheDados(true);
            } 
            else
                JOptionPane.showMessageDialog(null, "O campo \"Valor da Diária\" não permite letra", "Aviso", JOptionPane.WARNING_MESSAGE);
            
        } else {
            JOptionPane.showMessageDialog(null, "Existe campo sem preencher", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btCadastrarActionPerformed

