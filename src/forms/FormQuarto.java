    private Boolean verificaNumQuarto() {
        if (this.tfNumeroQuarto.getText().equals("    ")) {
            this.tfNumeroQuarto.requestFocus();
            return false;
        } else {
            return true;
        }
    }

    private Boolean verificaCategoria() {
        if (bgCategoria.getSelection() == null) {
            return false;
        } else {
            return true;
        }
    }

    private Boolean verificaNumCamas() {
        if (bgCamas.getSelection() == null) {
            return false;
        } else {
            return true;
        }
    }

    private Boolean verificaRadioButton() {
        if (verificaCategoria()) {
            return verificaNumCamas();
        }

        return false;
    }

    private Boolean verificaCampos() {
        if (verificaNumQuarto()) {
            return verificaRadioButton();
        }

        return false;
    }

    private void preencheDados(Boolean cadastra) {
        Quarto q = new Quarto();
        q.setNumQuarto(Short.parseShort(tfNumeroQuarto.getText()));
        q.setCategoria(bgCategoria.getSelection().getActionCommand());
        q.setNumCamas(Byte.parseByte(bgCamas.getSelection().getActionCommand()));
        q.setValor(Float.parseFloat(tfDiaria.getText()));
        
        if (cadastra)
            FormPrincipal.quartoDAO.adicionarQuarto(q);
        else
            FormPrincipal.quartoDAO.atualizarQuarto(q);
        
        
        JOptionPane.showMessageDialog(null, "Cadastro do quarto realizado com sucesso!", "Cadastro", JOptionPane.INFORMATION_MESSAGE);
        this.limparDados();
    }
    
    private void limparDados(){
        tfNumeroQuarto.setText("");
        bgCategoria.clearSelection();
        bgCamas.clearSelection();
        tfDiaria.setText("0.00");

        tfNumeroQuarto.requestFocus();
    }
    
    public void travaItens(){
        tfNumeroQuarto.setEnabled(false);
        btCadastrar.setEnabled(false);
        btBuscarCPF.setEnabled(false);
        btLimpar.setEnabled(false);
        btAtualizar.setEnabled(true);
    }
    
    public void destravaItens(){
        tfNumeroQuarto.setEnabled(true);
        btCadastrar.setEnabled(true);
        btBuscarCPF.setEnabled(true);
        btLimpar.setEnabled(true);
        btAtualizar.setEnabled(false);
    }

